#include "stdafx.h"
#include "SL_Time.h"

TIME::TIME()
{
	start = chrono::steady_clock::now();
	last = chrono::steady_clock::now();
}
//*///------------------------------------------------------------------------------------------
//*///------------------------------------------------------------------------------------------
chrono::steady_clock::time_point TIME::GetLast()
{
	return last;
}
//*///------------------------------------------------------------------------------------------
//*///------------------------------------------------------------------------------------------
chrono::steady_clock::time_point TIME::GetStart()
{
	return start;
}
//*///------------------------------------------------------------------------------------------
//*///------------------------------------------------------------------------------------------
void TIME::Update()
{
	last = chrono::steady_clock::now();
}
//*///------------------------------------------------------------------------------------------
//*///------------------------------------------------------------------------------------------
chrono::duration<int> TIME::GetFullDuration()
{
	return chrono::duration_cast<chrono::duration<int>>(start - chrono::steady_clock::now());
}
//*///------------------------------------------------------------------------------------------
//*///------------------------------------------------------------------------------------------
chrono::duration<int> TIME::GetInactiveTime()
{
	return chrono::duration_cast<chrono::duration<int>>(last - chrono::steady_clock::now());
}
//*///------------------------------------------------------------------------------------------
//*///------------------------------------------------------------------------------------------
chrono::duration<int> TIME::GetActiveDuration()
{
	return chrono::duration_cast<chrono::duration<int>>(start - last);
}
